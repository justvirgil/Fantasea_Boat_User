package com.example.androidintents.fantaseav2;
//used for travel destination
public class UserDestination {

    String activities, agency_id, agency_name, destination_name,destination_province, destination_pic;
    int base_price;

    public UserDestination() {

    }

    public String getActivities() {
        return activities;
    }

    public String getAgency_id() {
        return agency_id;
    }

    public String getAgency_name() {
        return agency_name;
    }

    public String getDestination_name() {
        return destination_name;
    }

    public String getDestination_province() {
        return destination_province;
    }

    public String getDestination_pic() {
        return destination_pic;
    }

    public int getBase_price() {
        return base_price;
    }
}

